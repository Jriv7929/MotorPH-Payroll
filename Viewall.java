    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
                
            DefaultTableModel model = (DefaultTableModel) allemp.getModel();
            String filePath = "C:\\Users\\SCHOOL\\Documents\\NetBeansProjects\\MotorPH_Employee_Application\\src\\empinfo2.csv";
            File file = new File(filePath);

            try (BufferedWriter bw = new BufferedWriter(new FileWriter(file))) {
                
            for (int i = 0; i < model.getRowCount(); i++) {
                StringBuilder row = new StringBuilder();
                for (int j = 0; j < model.getColumnCount(); j++) {
                    row.append(model.getValueAt(i, j));
                    if (j < model.getColumnCount() - 1) row.append(",");
                }
                bw.write(row.toString());
                bw.newLine();
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving to file.");
        }
            
        
    }//GEN-LAST:event_saveActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
// Get the currently selected row
int selectedRow = allemp.getSelectedRow();

if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select a row to update.");
    return;
}

// Get the table model
DefaultTableModel model = (DefaultTableModel) allemp.getModel();

// Safely get each value from the selected row
String newemp     = String.valueOf(model.getValueAt(selectedRow, 0)).trim();
String lastname   = String.valueOf(model.getValueAt(selectedRow, 1)).trim();
String firstname  = String.valueOf(model.getValueAt(selectedRow, 2)).trim();
String sss        = String.valueOf(model.getValueAt(selectedRow, 3)).trim();
String philhealth = String.valueOf(model.getValueAt(selectedRow, 4)).trim();
String tin        = String.valueOf(model.getValueAt(selectedRow, 5)).trim();
String pagibig    = String.valueOf(model.getValueAt(selectedRow, 6)).trim();

// Validate fields in the selected row
if (newemp.isEmpty() || lastname.isEmpty() || firstname.isEmpty() ||
    sss.isEmpty() || philhealth.isEmpty() || tin.isEmpty() || pagibig.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please complete all cells in the selected row.");
    return;
}

// âœ… Optional: reassign values in case you've processed/cleaned them
model.setValueAt(newemp,     selectedRow, 0);
model.setValueAt(lastname,   selectedRow, 1);
model.setValueAt(firstname,  selectedRow, 2);
model.setValueAt(sss,        selectedRow, 3);
model.setValueAt(philhealth, selectedRow, 4);
model.setValueAt(tin,        selectedRow, 5);
model.setValueAt(pagibig,    selectedRow, 6);
        
    }//GEN-LAST:event_updateActionPerformed
    
